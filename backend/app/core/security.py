# backend/app/core/security.py
from datetime import datetime, timedelta
from typing import Any, Union
from jose import jwt
from app.core.config import settings
import bcrypt

# --- เลิกใช้ passlib แล้วเขียนฟังก์ชันเองด้วย bcrypt ---

def create_access_token(subject: Union[str, Any], expires_delta: timedelta = None) -> str:
    if expires_delta:
        expire = datetime.utcnow() + expires_delta
    else:
        expire = datetime.utcnow() + timedelta(minutes=settings.ACCESS_TOKEN_EXPIRE_MINUTES)
    
    to_encode = {"exp": expire, "sub": str(subject)}
    encoded_jwt = jwt.encode(to_encode, settings.SECRET_KEY, algorithm=settings.ALGORITHM)
    return encoded_jwt

def verify_password(plain_password: str, hashed_password: str) -> bool:
    # แปลงรหัสผ่านเป็น bytes ก่อนตรวจสอบ
    password_byte = plain_password.encode('utf-8')
    hashed_password_byte = hashed_password.encode('utf-8')
    return bcrypt.checkpw(password_byte, hashed_password_byte)

def get_password_hash(password: str) -> str:
    # แปลงรหัสผ่านเป็น bytes แล้ว hash
    pwd_bytes = password.encode('utf-8')
    salt = bcrypt.gensalt()
    hashed = bcrypt.hashpw(pwd_bytes, salt)
    return hashed.decode('utf-8') # ส่งกลับเป็น string